package operatormod;

public class $5 {
    public static void main(String[] args) {

        int x, y, z;

        x = 7;
        y = 7;
        z = 8;

        System.out.println("Nilai Awal");
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);

        System.out.println("Operasi Logika &&");
        System.out.println("(x==y) && (++x==z) => " + ((x == y) && (++x == z)));
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
        System.out.println("(x==y) && (++x==z) => " + ((x == y) && (++x == z)));
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);

        x = 10;
        y = 10;
        z = 20;

        System.out.println("Nilai Awal");
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);

        System.out.println("Operasi Logika &");
        System.out.println("(x==y) & (++x==z) => " + ((x == y) & (++x == z)));
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
        System.out.println("(x==y) & (++x==z) => " + ((x == y) & (++x == z)));
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
    }
}
