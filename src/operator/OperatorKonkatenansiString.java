package operator;

public class OperatorKonkatenansiString {
    public static void main(String[] args) {
        System.out.println("Operator Konkatenasi String (+)");
        System.out.println("\"Andi\" + \"Budiman\" = " + ("Andi" + "Budiman"));
        System.out.println("\"Andi \" + \"Wijaya\" = " + ("Andi " + "Wijaya"));
        System.out.println("\"Nilai:\" + 3 = " + ("Nilai:" + 3));
        System.out.println("\"3\" + 3 = " + ("3" + 3));
        System.out.println("3 + \"4\" = " + (3 + "4"));
    }
}
