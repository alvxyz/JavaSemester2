package operatormod;

public class $14 {

    public static void main(String[] args) {
        System.out.println("Operator Konkatenasi String (+)");
        System.out.println("\"Gunung\" + \"Himalaya\" = " + ("Gunung" + "Himalaya"));
        System.out.println("\"Andi \" + \"Purnama\" = " + ("Andi " + "Purnama"));
        System.out.println("\"Nilai:\" + 10 = " + ("Nilai:" + 10));
        System.out.println("\"3\" + 2 = " + ("3" + 2));
        System.out.println("3 + \"4\" = " + (3 + "4"));
    }
}
